package rainbow;

public class RainFormat
{   
    public final String RESET       = "\u001B[0m";      // color reseter
    public final String BOLD        = "\u001B[1m";      // bold text
    public final String ITALIC      = "\u001B[3m";  	// italic text
    public final String UNDERLINE   = "\u001B[4m";      // under line text
    public final String BLINK       = "\u001B[5m";      // blink text
    public final String HIDDEN      = "\u001B[8m";      // hidden text
    public final String CUT         = "\u001B[9m";      // strikethrough text
    public final String DOUBLELINE  = "\u001B[21m";     // double line text
    public final String OVERLINE    = "\u001B[53m";     // overline text
}